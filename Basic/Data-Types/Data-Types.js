// JavaScript has several data types, including: 

// Strings: Used for text. Enclosed in single or double quotes. 

let name = "Alice";

console.log(name);


// Numbers: Used for numeric values (integers and floating-point numbers).

let age = 25;
let price = 9.99;

console.log(age, price);

// Booleans: Represents true or false values.

let isStudent = true;
let isAdmin = false;

console.log(isStudent, isAdmin);
