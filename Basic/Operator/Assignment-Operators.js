let num = 10;
num += 5; // Equivalent to num = num + 5
num -= 3; // Equivalent to num = num - 3
num *= 2; // Equivalent to num = num * 2
num /= 4; // Equivalent to num = num / 4



let x = 10;
let y = 3;

// Assignment operator (=)
let assignmentResult = x;
console.log("Assignment operator (x):", assignmentResult);

// Addition assignment operator (+=)
x += y;
console.log("Addition assignment (x += y):", x);

// Subtraction assignment operator (-=)
x -= y;
console.log("Subtraction assignment (x -= y):", x);

// Multiplication assignment operator (*=)
x *= y;
console.log("Multiplication assignment (x *= y):", x);

// Division assignment operator (/=)
x /= y;
console.log("Division assignment (x /= y):", x);

// Modulus assignment operator (%=)
x %= y;
console.log("Modulus assignment (x %= y):", x);
