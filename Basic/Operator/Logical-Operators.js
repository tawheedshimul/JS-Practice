let isTrue = true;
let isFalse = false;
let andResult = isTrue && isFalse; // Logical AND
let orResult = isTrue || isFalse; // Logical OR
let notResult = !isTrue; // Logical NOT


// practice 

let x = 10;
let y = 5;

// Logical AND (&&)
let logicalAndResult = x > 5 && y < 10;
console.log("Logical AND:", logicalAndResult);

// Logical OR (||)
let logicalOrResult = x >= 10 || y < 5;
console.log("Logical OR:", logicalOrResult);

// Logical NOT (!)
let logicalNotX = !(x > 5);
console.log("Logical NOT for x:", logicalNotX);

let logicalNotY = !(y < 5);
console.log("Logical NOT for y:", logicalNotY);
